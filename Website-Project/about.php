     <h2>A history of compassion
</h2>
<p>Founded in Montreal in 1869, the Society for the Prevention of Cruelty to Animals (now known simply as the Montreal SPCA) was the first animal-welfare organization in Canada. It is not known under whose influence the society was formed, but its history acknowledges assistance from animal-welfare societies in France, England and the United States. In its early years, the SPCA concentrated primarily on improving the treatment of work horses, which were used all across the island of Montreal. The SPCA later began working on additional issues, such as humane dog control, cock-fighting and the killing of wild birds. The SPCA has come a long way since its beginnings: today, it is the largest animal-protection organization in Quebec, speaking on behalf of animals wherever there is ignorance, cruelty, exploitation or neglect.</p>
